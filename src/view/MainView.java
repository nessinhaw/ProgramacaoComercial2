    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Cliente cliente = new Cliente();
        
        if(!txtId.getText().isEmpty()){
            cliente.setId(Integer.parseInt(txtId.getText()));
        }
        
        if(validarCampos() == true){
            if(cliente.getId() == 0){
                clienteController.create(txtNome.getText(), txtEmail.getText(), txtCPF.getText(), txtTelefone.getText(), txtAniversario.getText());
            } else {
                clienteController.update(Integer.valueOf(txtId.getText()), txtNome.getText(), txtEmail.getText(), txtCPF.getText(), txtTelefone.getText(), txtAniversario.getText());
            }
            
            limparCamposCliente();
            this.getListaClientes();

            jPanelMain.removeAll();
            jPanelMain.repaint();
            jPanelMain.revalidate();

            jPanelMain.add(jPanelListarClientes);
            jPanelMain.repaint();
            jPanelMain.revalidate();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

