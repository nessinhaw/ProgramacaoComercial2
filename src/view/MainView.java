    public void limparCamposCliente(){
        txtId.setText("");
        txtNome.setText("");
        txtEmail.setText("");
        txtCPF.setText("");
        txtTelefone.setText("");
        txtAniversario.setText("");
    }
    
    public boolean validarCampos(){
        boolean valido;
        if(txtNome.getText().equals("") || txtEmail.getText().equals("") || 
           txtCPF.getText().equals("") || txtTelefone.getText().equals("") || txtAniversario.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Todos os campos são obrigatórios!", "Erro", JOptionPane.ERROR_MESSAGE);
            valido = false;
        } else {
            valido = true;
        }
        return valido;
    }
    
    public void getListaClientes(){
        btnAtualizar.setEnabled(false);
        btnExcluir.setEnabled(false);
        
        DefaultTableModel modeloTabelaClientes = (DefaultTableModel) jTableClientes.getModel();
        modeloTabelaClientes.setNumRows(0);
        
        for (Cliente c : clienteController.read()) {
            modeloTabelaClientes.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getCpf(),
                c.getTelefone(),
                c.getDataAniversario()});
        }
    }
    
    public void getListaClientePorNome(){
        btnAtualizar.setEnabled(false);
        btnExcluir.setEnabled(false);
        
        DefaultTableModel modeloTabelaClientes = (DefaultTableModel) jTableClientes.getModel();
        modeloTabelaClientes.setNumRows(0);
        
        for (Cliente c : clienteController.getListaClientePorNome(txtBuscar.getText())) {
            modeloTabelaClientes.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getCpf(),
                c.getTelefone(),
                c.getDataAniversario()});
        }
    }
